// Helper function to get icon config
export const getIcon = (category, key) => {
  return iconMappings[category]?.[key] || {
    library: 'Feather',
    name: 'help-circle'
  };
};

// Helper function to get tab icon
export const getTabIcon = (tabName, focused = false) => {
  const icon = iconMappings.tabs[tabName];
  if (!icon) return { library: 'Feather', name: 'help-circle' };
  
  return {
    library: icon.library,
    name: focused && icon.focusedName ? icon.focusedName : icon.name
  };
};

// Helper function to get stat icon
export const getStatIcon = (statName) => {
  return iconMappings.stats[statName] || {
    library: 'Feather',
    name: 'activity'
  };
};

// Helper function to get action icon
export const getActionIcon = (actionName) => {
  return iconMappings.actions[actionName] || {
    library: 'Feather',
    name: 'more-horizontal'
  };
};